package com.ws.msp.mq.sac.pojo;

import com.ws.pojo.GenericBean;


public class RestResult<T> extends GenericBean {
	public static final int SUCCESS = 0;
	/**
	 * 
	 */
	private static final long serialVersionUID = -3638205431044065510L;

	private int code = SUCCESS;
	private boolean success = true;
	private String message = null;
	private T data = null;

	/**
	 * @return the code
	 */
	public int getCode() {
		return code;
	}

	/**
	 * @param code
	 *            the code to set
	 */
	public void setCode(int code) {
		this.code = code;
	}

	/**
	 * @return the success
	 */
	public boolean isSuccess() {
		return success;
	}

	/**
	 * @param success
	 *            the success to set
	 */
	public void setSuccess(boolean success) {
		this.success = success;
	}

	/**
	 * @return the message
	 */
	public String getMessage() {
		return message;
	}

	/**
	 * @param message
	 *            the message to set
	 */
	public void setMessage(String message) {
		this.message = message;
	}

	/**
	 * @return the data
	 */
	public T getData() {
		return data;
	}

	/**
	 * @param data
	 *            the data to set
	 */
	public void setData(T data) {
		this.data = data;
	}

}
